                        <p>Minuman kopi sudah menjadi salah satu minuman yang paling digemari dan dicari di dunia. Identik dengan rasa pahit yang justru menjadi candu bagi kebanyakan orang. 
                            Seiring dengan berjalannya waktu dan pesat nya penyebaran biji kopi ke pelosok penjuru dunia, tradisi meminum kopi yang berasal dari dunia barat pun akhirnya 
                            diadaptasi oleh masyarakat di Indonesia. Kopi mulai dikenal di Indonesia pada tahun 1696 pada saat Walikota Amsterdam, Nicholas Witsen, memerintahkan komandan
                            pasukan Belanda di Pantai Malabar, Adrian Van Ommen untuk membawa biji kopi ke Batavia. Di Negara-negara barat, meminum kopi di pagi hari bahkan sudah menjadi
                            semacam ritual dan budaya. Bahkan di Amerika, kopi menjadi minuman tradisional bagi rakyatnya. Kopi menjadi menu untuk minuman pagi, sore, dan malam hari.
                            Semua kalangan dan semua lapisan masyarakat gemar menyeruput minuman hitam ini karena memang aroma dan rasanya yang luar biasa. Meminum kopi atau ngopi menjadi
                            bagian dari gaya hidup dan menjelma menjadi budaya di Indonesia. Kopi dapat dikatakan menjadi teman ngumpul dan juga identitas diri bagi sebagian masyarakat.
                            Kopi tidak lagi diposisikan sebagai minuman yang secara harfiah diartikan sebagai cairan untuk menghilangkan dahaga. Kopi kini memposisikan dirinya lebih dari 
                            sekedar minuman, sebagai simbol budaya, adat istiadat, tradisi, serta gaya hidup masyarakat modern.</p>
